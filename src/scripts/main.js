const animado1 = document.getElementsByClassName('animation__item')[0]
const animador1 = document.getElementsByClassName('animation')[0]
animador1.addEventListener('mouseenter',function () {  
    animado1.classList.add('item-animado')
})
animador1.addEventListener('mouseleave',function () {  
    animado1.classList.remove('item-animado')
})

